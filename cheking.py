if __name__ == "__main__":
    import shutil

    # בדיקת מקום פנוי בכונן הנוכחי באמצעות פייתון (עוקף את חסימת האבטחה)
    total, used, free = shutil.disk_usage(".")
    
    free_gb = free / (1024 ** 3) # המרה מבייטים לג'יגה-בייט
    print(f"--- בדיקת מערכת ---")
    print(f"מקום פנוי בכונן: {free_gb:.2f} GB")
    print(f"-------------------\n")
    
    # אם יש פחות מ-5GB פנויים, נזהיר מראש
    if free_gb < 5:
        print("[!] אזהרה: המקום בכונן נמוך מאוד. כדאי לפנות מקום לפני הריצה המלאה.")
    
    